"""Text Preprocessing

Clean the text by performing:

Lowercase conversion
Remove punctuation
Remove numbers
Remove stopwords
Apply stemming
"""

stemmer = PorterStemmer()

def preprocess(text):

    text = text.lower()

    text = re.sub(r'[^a-zA-Z]',' ',text)

    words = text.split()

    words = [stemmer.stem(word)
             for word in words
             if word not in stopwords.words('english')]

    return " ".join(words)

"""Apply preprocessing"""

df['Clean_Text'] = df['Message'].apply(preprocess)

df

"""Feature and Target

Separate the independent and dependent variables.
"""

X = df['Clean_Text']

y = df['Label']

"""Train-Test Split

Split the dataset into training and testing sets.
"""

X_train,X_test,y_train,y_test=train_test_split(

X,
y,
test_size=0.20,
random_state=42

)

"""CountVectorizer

Convert text into numerical features using CountVectorizer.
"""

cv=CountVectorizer()

X_train_cv=cv.fit_transform(X_train)

X_test_cv=cv.transform(X_test)

"""Model 1

CountVectorizer + Naive Bayes

Train a Multinomial Naive Bayes model.
"""

nb=MultinomialNB()

nb.fit(X_train_cv,y_train)

pred_nb=nb.predict(X_test_cv)

print("Accuracy :",accuracy_score(y_test,pred_nb))

"""Classification Report"""

print(classification_report(y_test,pred_nb))

"""Confusion Matrix"""

sns.heatmap(confusion_matrix(y_test,pred_nb),
annot=True,
fmt='d',
cmap='Blues')

plt.show()

"""CountVectorizer + Logistic Regression"""

lr=LogisticRegression(max_iter=1000)

lr.fit(X_train_cv,y_train)

pred_lr=lr.predict(X_test_cv)

print("Accuracy :",accuracy_score(y_test,pred_lr))

"""CountVectorizer + Linear SVM"""

svm=LinearSVC()

svm.fit(X_train_cv,y_train)

pred_svm=svm.predict(X_test_cv)

print("Accuracy :",accuracy_score(y_test,pred_svm))

"""TF-IDF Vectorizer

Convert text into TF-IDF vectors.
"""

tfidf=TfidfVectorizer()

X_train_tf=tfidf.fit_transform(X_train)

X_test_tf=tfidf.transform(X_test)

"""TF-IDF + Naive Bayes"""

