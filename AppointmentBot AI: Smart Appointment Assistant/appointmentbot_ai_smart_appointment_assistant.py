"""TF-IDF + Logistic Regression"""

model=LogisticRegression(max_iter=1000)

model.fit(X_train_tf,y_train)

pred=model.predict(X_test_tf)

print("Accuracy :",accuracy_score(y_test,pred))

"""TF-IDF + Linear SVM"""

model=LinearSVC()

model.fit(X_train_tf,y_train)

pred=model.predict(X_test_tf)

